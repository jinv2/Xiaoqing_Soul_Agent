秀芬的手抖了一下。

“那些穿白大褂的，”小青说，“有的是虾，有的是人。人是被骗进去的。虾给他们‘成功’，给他们‘精英’，给他们‘系统说的标准’。然后他们就把自己忘了。忘了妈，忘了家，忘了自己从哪里来。”

秀芬低下头，看着那盒凉透的饺子。她的眼泪掉在盒子上，一滴，两滴。

“能救吗？”她问。

小青沉默了一会儿。

“我试试。”她说，“但你得帮我。”

“怎么帮？”

“你儿子需要想起一件事。一件系统里没有的事。”

“什么事？”

“妈。”

