她们坐在台阶上。太阳慢慢升高，照在两个人——一个人，一个魂魄——身上。

秀芬把布袋打开，给小青看里面的东西。奖状，全家福，凉透的饺子。

“我儿子，”她说，“以前学中医的。背《伤寒论》能背到半夜，背给我听。后来进了这里，就不回家了。上次发消息，说他是‘年度精英医生’，说‘系统说’他成功了。”

小青看着那张全家福。照片里的男孩十几岁，笑得眼睛弯弯的，手里捧着一张奖状。

“他叫什么？”

