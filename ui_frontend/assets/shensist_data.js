“姐……”

“去吧。去帮人。救人。被人记住。”白蛇的手慢慢松开，一点一点，像是放开了一千年的牵挂，“这样……我们就没有白活。”

她从枕下拿出一只碗。粗瓷，旧碗，碗底刻着一个字：**青**。

“这是我给自己刻的。”白蛇说，“怕忘了自己是谁。现在给你。有了它，就知道自己是谁。”

小青接过碗，说不出话。

白蛇的眼睛闭上了。

烛火晃了一下，没有灭。

小青跪着，跪了一夜。天亮时，雪停了。她站起来，把碗收进怀里，走出门。

门外是人间。阳光照在她脸上，暖暖的。有炊烟从远处的村庄升起，有孩子在雪地里奔跑。

她不知道，东海深处，一只巨大的龙虾睁开了眼睛。它身边有无数的虾兵蟹将，还有几个穿着白大褂的人，眼神空洞，站在虾群里。

那只虾开口说话，声音像一万块石头在摩擦：

“等了一千年。该我上场了。”

